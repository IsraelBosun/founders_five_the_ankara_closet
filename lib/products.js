export function getProductBySlug(slug) {
  return products.find((p) => p.slug === slug) || null;
}

export function getProductsByCategory(category) {
  return products.filter((p) => p.category === category);
}

export function getFeaturedProducts() {
  return products.filter((p) => p.featured);
}

export function getRelatedProducts(currentSlug, limit = 4) {
  return products.filter((p) => p.slug !== currentSlug).slice(0, limit);
}

export function formatPrice(amount) {
  return `₦${amount.toLocaleString("en-NG")}`;
}

export const WHATSAPP_NUMBER = "2348133053455";

export function getWhatsAppOrderLink(productName, price) {
  const message = encodeURIComponent(
    `Hello! I’d like to order the *${productName}* (${formatPrice(price)}). Please send me details on sizes, colours and delivery. Thank you! 🙏`
  );
  return `https://wa.me/${WHATSAPP_NUMBER}?text=${message}`;
}

export function getWhatsAppGeneralLink() {
  const message = encodeURIComponent(
    "Hello! I’d like to browse your collection and place an order. Please assist me 🙏"
  );
  return `https://wa.me/${WHATSAPP_NUMBER}?text=${message}`;
}
